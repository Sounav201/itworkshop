#Write a program which takes three numbers as input find maximum of three and minimum of the three.
a=int(input("Enter a number "))
b=int(input("Enter a number "))
c=int(input("Enter a number "))

print(str(max(a,b,c)) + " is the maximum of the three numbers. " )
print(str(min(a,b,c)) + " is the minimum of the three numbers. " )

